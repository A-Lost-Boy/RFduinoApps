/*
 * Write the given character at the current display position.
 * This method is required from the Print class which is the parent
 * of this (OLEDDisplay) class.
 *
 * If the character is a newline '\n' drop a row and return to the left.
 * Carriage returns are ignored.
 */
size_t OLEDDisplay::write(uint8_t c)
{
	// If newline, drop down a line and go back to beginning of the line
	if (c == '\n') {
		_y += getCharacterHeight();
		_x = 0;
	}
	else if (c == '\r') {
		// skip carriage returns
	}
	else {
		// Draw the character at the current location
		this->drawChar(_x, _y, c, WHITE, BLACK, _textSize);

		// Update the location.
		// If at the end of the line, wrap to the next one if wrap is one
		_x += getCharacterWidth();
		if (_wordWrap && (_x > (SSD1306_LCDWIDTH - getCharacterWidth() ))) {
			_y += getCharacterHeight();
			_x = 0;
		}
	}
}

/*
 * Draw the given character at the given location.
 * The (x, y) location is the upper left of the bounding box of the character.
 */
void OLEDDisplay::drawChar(int16_t x, int16_t y, unsigned char c,
	uint16_t color, uint16_t bg, uint8_t size) 
{
	// If the character is totally off the display, don't draw it
	if ((x >= SSD1306_LCDWIDTH) ||  // Clip right
		(y >= SSD1306_LCDHEIGHT) || // Clip bottom
		((x + 6 * size - 1) < 0) || // Clip left
		((y + 8 * size - 1) < 0))   // Clip top
	{
		return;
	}

	for (int8_t i = 0; i<6; i++) 
	{
		uint8_t line;
		if (i == 5)
			line = 0x0;
		else
			line = pgm_read_byte_near(font + (c * 5) + i);
		for (int8_t j = 0; j<8; j++) {
			if (line & 0x1) 
			{
				if (size == 1) // default size
					drawPixel(x + i, y + j, color);
				else {  // big size
					fillRect(x + (i*size), y + (j*size), size, size, color);
				}
			}
			else if (bg != color) 
			{
				if (size == 1) // default size
					drawPixel(x + i, y + j, bg);
				else {  // big size
					fillRect(x + i*size, y + j*size, size, size, bg);
				}
			}
			line >>= 1;
		}
	}
}

/*
* Draw the given character at the given location.
* The (x, y) location is the upper left of the bounding box of the character.
*/
void OLEDDisplay::drawCharFont2(int16_t x, int16_t y, unsigned char c,
	uint16_t color, uint16_t bg, uint8_t size)
{
	// If the character is totally off the display, don't draw it
	if ((x >= SSD1306_LCDWIDTH) ||  // Clip right
		(y >= SSD1306_LCDHEIGHT) || // Clip bottom
		((x + 6 * size - 1) < 0) || // Clip left
		((y + 8 * size - 1) < 0))   // Clip top
	{
		return;
	}

	int base = (c - 32) * 13;

	Serial.print(c);
	Serial.print(" ");
	Serial.print(base);
	Serial.println();

	y += 12;
	for (int8_t i = 0; i < 13; i++)
	{
		uint8_t line = pgm_read_byte_near(font2 + (base + i));
		Serial.print(i);
		Serial.print(" = ");
		Serial.print(line, HEX);
		Serial.print(" bits:");

		for (int8_t j = 8; 0 < j; j--) 
		{
			if (line & 0x1)
			{
				Serial.print(" 1");
				if (size == 1) // default size
					drawPixel(x + j, y - i, color);
				else {  // big size
					fillRect(x + (j*size), y - (i*size), size, size, color);
				}
			}
			else if (bg != color) 
			{
				Serial.print(" 0");
				if (size == 1) // default size
					drawPixel(x + j, y - i, bg);
				else {  // big size
					fillRect(x + j*size, y - i*size, size, size, bg);
				}

			}
			line >>= 1;
		}
		Serial.println();
	}
}

/*
 * Draw the given bitmap at the x, y position using the given foreground and background colors.
 * The bitmap buffer must be in PROGMEM (flash) memory.
 * The (x, y) location is the upper left hand corner of where the bitmap is drawn.
 */
void OLEDDisplay::drawBitmap(uint x, uint y,
	const uint8_t *bitmap, 
	uint width, uint height,
	uint16_t color, uint16_t bg) 
{
	int16_t i, j, byteWidth = (width + 7) / 8;

	for (j = 0; j < height; j++) 
	{
		for (i = 0; i < width; i++) 
		{
			if (pgm_read_byte_near(bitmap + j * byteWidth + i / 8) & (128 >> (i & 7)))
			{
				drawPixel(x + i, y + j, color);
			}
			else {
				drawPixel(x + i, y + j, bg);
			}
		}
	}
}

/*
 * Returns the current character width for the textSize set.
 */
uint OLEDDisplay::getCharacterWidth()
{
	return _textSize * 6;
}

/*
* Returns the current character height for the textSize set.
*/
uint OLEDDisplay::getCharacterHeight()
{
	return _textSize * 8;
}

/*
 * Write the given command to the display.
 *
 * A command is written to the I2C bus by writting the display address
 * and follow that by the control message byte of 0. The command follows
 * the control byte and then communication is terminated.
 *
 * There isn't a reply to the command from the display.
 */
void OLEDDisplay::writeCommand(uint8_t command)
{
	uint8_t control = 0x00;   // Co = 0, D/C = 0
	Wire.beginTransmission(_address);
	Wire.write(control);
	Wire.write(command);
	Wire.endTransmission();
}
